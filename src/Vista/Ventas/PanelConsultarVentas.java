    private void Btn_VerTodoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Btn_VerTodoActionPerformed
        // Realizar una consulta de todas la ventas e imprimir en tabla
        ArrayList<Venta> listaVentas;
        //Mandar a llamar al metodo de la clase controlador que realiza la consulta
        listaVentas=controlador.consultarTodas();
         
        //Imprimir en tabla. 
        modelo.setRowCount(0);
        Object[] datosFila = new Object[3]; //El tama√±o de la lista es igual al numero de columnas (3)
        //Recorrer lista de resultados objenidos de la consulta y agregarlo a la lista de filas de la tabla
        for(Venta v : listaVentas){
            datosFila[0]= v.getFolio_venta();
            datosFila[1]= v.getFecha();
            datosFila[2]= v.getTotal();
            modelo.addRow(datosFila);
        }
        //Se dibuja el modelo en la tabla
        Tabla.setModel(modelo);
        
    }//GEN-LAST:event_Btn_VerTodoActionPerformed


